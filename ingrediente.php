<b>MOD DE PREPARARE </b><br>
<b>O supă-cremă de legume foarte gustoasă care se face repede şi uşor. Iată reţeta: </b><br>

1.   Se spală bine legumele şi se curăţă. Se taie ceapa peştişori şi se pune la călit <br> în unt. <br>
<br>
2.   Peste ea, se adaugă restul legumelor tăiate cubuleţe şi se asezonează cu sare, piper şi boia.<br> 
Peste acestea, se toarnă apă şi se lasă câteva minute să fiarbă. <br>
<br>
3.   Între timp, se pregătesc crutoanele. Se taie cuburi feliile de pâine şi se așază într-o tavă <br> 
tapetată cu hârtie de copt. Se dau la cuptor pentru câteva minute, după care se scot şi se stropesc <br> 
cu ulei de măsline şi cu usturoiul pisat. Se mai dau la cuptor pentru câteva minute, până se rumenesc.<br>
<br>
4.   Când legumele sunt fierte, se scot din zeamă şi se dau prin blender. Compoziţia obţinută se pune <br> 
într-o altă oală, iar peste ea se adaugă puţin câte puţin din apa în care au fiert, până se obţine consistenţa <br> 
dorită. Se serveşte cu crutoane şi pătrunjel tocat mărunt. <br></p>
